import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const generatedDir = path.join(root, 'src', 'generated');
const outputPath = path.join(generatedDir, 'offering-contracts.js');

function readArtifact(contractPath, name) {
  const artifact = JSON.parse(fs.readFileSync(path.join(root, 'out', contractPath, name + '.json'), 'utf8'));
  return {
    abi: artifact.abi,
    bytecode: artifact.bytecode.object,
  };
}

function existingFactoryAddress() {
  if (!fs.existsSync(outputPath)) return '';
  const text = fs.readFileSync(outputPath, 'utf8');
  const match = text.match(/OFFERING_FACTORY_ADDRESS = '([^']*)'/);
  return match ? match[1] : '';
}

const contracts = {
  OFFERING_FACTORY_ADDRESS: existingFactoryAddress(),
  OfferingFactory: readArtifact('OfferingFactory.sol', 'OfferingFactory'),
  Offering: readArtifact('Offering.sol', 'Offering'),
};

fs.mkdirSync(generatedDir, { recursive: true });
fs.writeFileSync(outputPath, `// Generated by scripts/export-contracts.js. Do not edit by hand.
export const OFFERING_FACTORY_ADDRESS = '${contracts.OFFERING_FACTORY_ADDRESS}';
export const OFFERING_FACTORY_ABI = ${JSON.stringify(contracts.OfferingFactory.abi, null, 2)};
export const OFFERING_FACTORY_BYTECODE = '${contracts.OfferingFactory.bytecode}';
export const OFFERING_ABI = ${JSON.stringify(contracts.Offering.abi, null, 2)};
`);

console.log('Wrote src/generated/offering-contracts.js');
